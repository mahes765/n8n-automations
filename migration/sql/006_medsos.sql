  created_at timestamptz not null default now()
);

create index if not exists medsos_request_events_request_created_idx
  on public.medsos_request_events(request_id, created_at desc);

create index if not exists medsos_request_events_event_type_idx
  on public.medsos_request_events(event_type);

create or replace function public.touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

