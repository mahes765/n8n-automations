create table if not exists public.medsos_packages (
  id bigint generated by default as identity primary key,
  code text not null unique,
  name text not null unique,
  financial_plan_id bigint unique references public.subscription_plans(id) on delete set null,
  purchase_type text not null default 'one_time' check (purchase_type = 'one_time'),
  description text,
  price integer not null check (price >= 0),
  quota_limit integer not null check (quota_limit > 0),
  validity_days integer not null check (validity_days > 0),
  features jsonb not null default '[]'::jsonb,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.medsos_entitlements (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  transaction_id bigint not null unique references public.transactions(id) on delete restrict,
  package_id bigint not null references public.medsos_packages(id) on delete restrict,
  status medsos_entitlement_status not null default 'pending_payment',
  quota_total integer not null check (quota_total > 0),
  quota_used integer not null default 0 check (quota_used >= 0),
  activated_at timestamptz,
  expires_at timestamptz,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint medsos_entitlements_quota_used_lte_total check (quota_used <= quota_total)
);

create index if not exists medsos_entitlements_user_status_idx
  on public.medsos_entitlements(user_id, status);

create index if not exists medsos_entitlements_package_status_idx
  on public.medsos_entitlements(package_id, status);

create table if not exists public.medsos_requests (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  entitlement_id bigint not null references public.medsos_entitlements(id) on delete cascade,
  platform medsos_platform not null,
  profile_url text not null,
  notes text,
  status medsos_request_status not null default 'queued',
  progress_percent integer not null default 0 check (progress_percent >= 0 and progress_percent <= 100),
  current_step text,
  n8n_execution_id text,
  callback_token text not null default encode(gen_random_bytes(24), 'hex'),
  retry_count integer not null default 0 check (retry_count >= 0),
  max_retries integer not null default 3 check (max_retries >= 0),
  quota_charged boolean not null default true,
  requested_at timestamptz not null default now(),
  started_at timestamptz,
  completed_at timestamptz,
  failed_at timestamptz,
  error_code text,
  error_message text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists medsos_requests_user_status_created_idx
  on public.medsos_requests(user_id, status, created_at desc);

create index if not exists medsos_requests_entitlement_idx
  on public.medsos_requests(entitlement_id);

create index if not exists medsos_requests_n8n_execution_idx
  on public.medsos_requests(n8n_execution_id);

create index if not exists medsos_requests_status_progress_idx
  on public.medsos_requests(status, progress_percent);

create table if not exists public.medsos_analysis_results (
  id bigint generated by default as identity primary key,
  request_id bigint not null unique references public.medsos_requests(id) on delete cascade,
  summary text,
  sentiment_label text,
  sentiment_score numeric(5, 4),
  sentiment_breakdown jsonb not null default '{}'::jsonb,
  engagement_score numeric(5, 2),
  engagement_metrics jsonb not null default '{}'::jsonb,
  top_topics jsonb not null default '[]'::jsonb,
  audience_insight jsonb not null default '{}'::jsonb,
  recommendations jsonb not null default '[]'::jsonb,
  charts_data jsonb not null default '{}'::jsonb,
  raw_payload jsonb,
  model_version text,
  generated_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists medsos_analysis_results_generated_at_idx
  on public.medsos_analysis_results(generated_at desc);

create table if not exists public.medsos_artifacts (
  id bigint generated by default as identity primary key,
  request_id bigint not null references public.medsos_requests(id) on delete cascade,
  artifact_type medsos_artifact_type not null,
  storage_provider text not null,
  storage_path text not null,
  public_url text,
  checksum text,
  payload jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists medsos_artifacts_request_idx
  on public.medsos_artifacts(request_id, artifact_type);

create table if not exists public.medsos_request_events (
  id bigint generated by default as identity primary key,
  request_id bigint not null references public.medsos_requests(id) on delete cascade,
  event_type text not null,
